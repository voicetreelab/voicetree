#!/usr/bin/env node
// Postinstall: replace node_modules/.bin/vitest with a shim that routes
// `npx vitest ...` (and any other resolution that lands at .bin/vitest)
// through scripts/run-remote.mjs by default. Opt-out: VT_REMOTE_EXEC=1
// (set automatically by run-remote.mjs over ssh, so nested invocations
// on the devbox short-circuit to the real vitest).
//
// Idempotent: detects an existing shim by a sentinel marker. Safe to
// re-run on every install.

import fs from 'node:fs'
import path from 'node:path'
import {fileURLToPath} from 'node:url'

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = path.resolve(SCRIPT_DIR, '..')
const BIN_PATH = path.join(REPO_ROOT, 'node_modules', '.bin', 'vitest')
const SENTINEL = 'VT_VITEST_SHIM_ENTRY'

if (!fs.existsSync(path.join(REPO_ROOT, 'node_modules', 'vitest', 'vitest.mjs'))) {
  console.log('[install-vitest-shim] vitest not installed; skipping')
  process.exit(0)
}

if (fs.existsSync(BIN_PATH)) {
  const stat = fs.lstatSync(BIN_PATH)
  if (!stat.isSymbolicLink()) {
    try {
      const head = fs.readFileSync(BIN_PATH, 'utf8').slice(0, 256)
      if (head.includes(SENTINEL)) {
        console.log('[install-vitest-shim] already installed')
        process.exit(0)
      }
    } catch {}
  }
  fs.rmSync(BIN_PATH, {force: true})
}

const shim = `#!/usr/bin/env node
// ${SENTINEL} — generated by scripts/install-vitest-shim.mjs.
// Routes vitest invocations through scripts/run-remote.mjs unless
// VT_REMOTE_EXEC=1 is set (recursion guard / explicit opt-out).
const path = require('path')
const {spawnSync} = require('child_process')

const SHIM_DIR = __dirname
const REPO_ROOT = path.resolve(SHIM_DIR, '..', '..')
const REAL_VITEST = path.join(REPO_ROOT, 'node_modules', 'vitest', 'vitest.mjs')
const args = process.argv.slice(2)

if (process.env.VT_REMOTE_EXEC === '1') {
  const r = spawnSync('node', [REAL_VITEST, ...args], {stdio: 'inherit'})
  process.exit(r.status ?? 0)
}

const RUN_REMOTE = path.join(REPO_ROOT, 'scripts', 'run-remote.mjs')
const r = spawnSync('node', [RUN_REMOTE, 'npx', 'vitest', ...args], {stdio: 'inherit'})
process.exit(r.status ?? 0)
`

fs.writeFileSync(BIN_PATH, shim)
fs.chmodSync(BIN_PATH, 0o755)
console.log('[install-vitest-shim] installed shim at', BIN_PATH)
